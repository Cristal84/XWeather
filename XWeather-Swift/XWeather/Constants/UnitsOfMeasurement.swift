import Foundation

enum TemperatureUnit : Int {
	case fahrenheit
	case celsius
}

enum DistanceUnit : Int {
	case miles
	case kilometers
}

enum PressureUnit : Int {
	case inchesOfMercury
	case millibars
}

enum LengthUnit : Int {
	case inches
	case millimeters
}

enum SpeedUnit : Int {
	case milesPerHour
	case kilometersPerHour
}

